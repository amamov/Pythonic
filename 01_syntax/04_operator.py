a, b = 7, 5
print(a + b)  # 12
print(a - b)  # 2
print(a * b)  # 35
print(a / b)  # 1.4
print(a // b)  # a를 b로 나누었을 때 몫 : 1
print(a % b)  # a를 b로 나누었을 때 나머지 : 2
print(a ** b)  # a^b : 16807
pow(a, b)  # 16807

c = -7
abs(c)  # 7

x = 10.0
y = 10
_y = 10
print(type(x))  # <class 'float'>
print(type(y))  # <class 'int'>
print(x == y)  # True : 객체의 값을 비교한다.
print(x is y)  # False : 객체의 주소를 비교한다.
print(y is _y)  # True

